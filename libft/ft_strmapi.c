#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				length;
	char			*mapped;

	i = 0;
	length = ft_strlen(s);
	mapped = (char *)malloc((length + 1) * sizeof(char));
	if (!mapped)
		return ((void *)0);
	while (s[i])
	{
		mapped[i] = f(i, s[i]);
		i++;
	}
	mapped[i] = '\0';
	return (mapped);
}
